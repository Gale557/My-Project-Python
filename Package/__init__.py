def function_sum(num1, num2):
    result = num1 + num2
    print(result)


def function_substraction(num1, num2):
    result = num1 - num2
    print(result)


def function_multiple(num1, num2):
    result = num1 * num2
    print(result)


def function_divide(num1, num2):
    result = num1 / num2
    print(result)
